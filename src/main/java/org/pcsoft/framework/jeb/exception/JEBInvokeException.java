public class JEBInvokeException extends JEBException {

    public JEBInvokeException() {
    }

    public JEBInvokeException(Throwable cause) {
        super(cause);
    }

    public JEBInvokeException(String message) {
        super(message);
    }

    public JEBInvokeException(String message, Throwable cause) {
        super(message, cause);
    }
}
