public class JEBConfigurationException extends JEBException {

    public JEBConfigurationException() {
    }

    public JEBConfigurationException(Throwable cause) {
        super(cause);
    }

    public JEBConfigurationException(String message) {
        super(message);
    }

    public JEBConfigurationException(String message, Throwable cause) {
        super(message, cause);
    }
}
