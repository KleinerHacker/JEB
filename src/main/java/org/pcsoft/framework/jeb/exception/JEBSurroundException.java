package org.pcsoft.framework.jeb.exception;

public class JEBSurroundException extends JEBException {

    public JEBSurroundException() {
    }

    public JEBSurroundException(Throwable cause) {
        super(cause);
    }

    public JEBSurroundException(String message) {
        super(message);
    }

    public JEBSurroundException(String message, Throwable cause) {
        super(message, cause);
    }
}
