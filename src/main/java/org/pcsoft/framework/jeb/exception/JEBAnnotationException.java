package org.pcsoft.framework.jeb.exception;

public class JEBAnnotationException extends JEBException {

    public JEBAnnotationException() {
    }

    public JEBAnnotationException(Throwable cause) {
        super(cause);
    }

    public JEBAnnotationException(String message) {
        super(message);
    }

    public JEBAnnotationException(String message, Throwable cause) {
        super(message, cause);
    }
}
